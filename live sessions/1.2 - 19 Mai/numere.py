# operatori aritmetici: 
print(2 + 2)
print(2 - 2)
print(2 * 14) # operatorul de inmultire
print(3 / 2) # impartirea a 2 numere
print(8 // 3) # floor division -> returneaza catul impartirii
print(3 ** 4) # ridicarea la putere -> 3 la puterea 4
print(5 % 2) # operatia modulo -> returneaza restul impartirii

# operatori de comparare: 
# ==; !=; >; <; >=; <=

print(2 == 3) # False
print(2 != 3) # True
print(10 > 4) # True
print(4 < 10) # True
print(5 >= 2) # True
print(5 <= 2) # False

